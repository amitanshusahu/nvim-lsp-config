vim.g.mapleader = " "

-- COMPILE AND RUN
function codeRunner()
    -- java
    if vim.bo.filetype == "java" then
        vim.cmd[[!javac % && java %]]
    elseif vim.bo.filetype == "cpp" then
        vim.cmd[[!g++ % -o run && ./run]]
    else
        print("scripts not written for this filetype")
        vim.cmd[[termianl]]
    end
end
vim.keymap.set('n', '<F9>', codeRunner)

